-- Adiciona suporte para insumos especiais nas fichas técnicas.
ALTER TABLE public.fichas_tecnicas ADD COLUMN IF NOT EXISTS insumo_especial_id bigint;

-- Adiciona a chave estrangeira (foreign key)
ALTER TABLE public.fichas_tecnicas DROP CONSTRAINT IF EXISTS fichas_tecnicas_insumo_especial_id_fkey;
ALTER TABLE public.fichas_tecnicas ADD CONSTRAINT fichas_tecnicas_insumo_especial_id_fkey 
    FOREIGN KEY (insumo_especial_id) REFERENCES public.insumos_especiais(id) ON DELETE SET NULL;

-- Torna a coluna 'insumo_id' nula, pois um item pode ser ou comum ou especial.
