    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    nome text NOT NULL
);

-- Habilita Row Level Security (RLS)
ALTER TABLE public.insumos_especiais ENABLE ROW LEVEL SECURITY;

-- Cria políticas de acesso para garantir que os usuários só acessem seus próprios dados.
DROP POLICY IF EXISTS "Enable access for authenticated users" ON public.insumos_especiais;
CREATE POLICY "Enable access for authenticated users" ON public.insumos_especiais
    FOR ALL
    USING (auth.role() = 'authenticated')
    WITH CHECK (auth.role() = 'authenticated');

-- 2. Cria a tabela 'insumos_especiais_opcoes'
-- Associa as categorias de insumos especiais aos insumos reais.
CREATE TABLE IF NOT EXISTS public.insumos_especiais_opcoes (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at timestamp with time zone DEFAULT now() NOT NULL,
    insumo_especial_id bigint NOT NULL,
    insumo_id bigint NOT NULL,
    CONSTRAINT insumos_especiais_opcoes_insumo_especial_id_fkey FOREIGN KEY (insumo_especial_id) REFERENCES public.insumos_especiais(id) ON DELETE CASCADE,
    CONSTRAINT insumos_especiais_opcoes_insumo_id_fkey FOREIGN KEY (insumo_id) REFERENCES public.insumos(id) ON DELETE CASCADE
);

-- Habilita RLS
ALTER TABLE public.insumos_especiais_opcoes ENABLE ROW LEVEL SECURITY;

-- Cria políticas de acesso
