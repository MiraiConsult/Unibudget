-- =================================================================
-- FUNCTION TO UPDATE FICHA TECNICA QUANTITIES
-- Bypasses client-side schema cache issues by using RPC.
-- =================================================================

CREATE OR REPLACE FUNCTION public.update_ficha_tecnica_quantities(
    p_id bigint,
    p_quantidade numeric DEFAULT NULL,
    p_quantidade_adulto numeric DEFAULT NULL,
    p_quantidade_infantil numeric DEFAULT NULL
)
RETURNS void
LANGUAGE plpgsql
AS $$
BEGIN
    UPDATE public.fichas_tecnicas
    SET
        quantidade = p_quantidade,
        quantidade_adulto = p_quantidade_adulto,
        quantidade_infantil = p_quantidade_infantil
    WHERE id = p_id;
END;
$$;

